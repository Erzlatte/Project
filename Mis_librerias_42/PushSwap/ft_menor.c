#include "push_swap.h"

int	ft_menor(char *s1, char *s2)
{
	int		i;
	char	c;

	i = 0;
	c = *s1;
	if (((int)c == 45))
	{
		if (s1[1] > s2[1])
			return (0);
		return (1);
	}
	if (s1[i] < s2[i])
		return (0);
	return (1);
}
