#include "push_swap.h"

int	ft_isdigit(char *s)
{
	char	c;

	c = *s;
	if ((int)c >= 48 && (int)c <= 57)
		return (1);
	if (((int)c == 45))
	{
		c = s[1];
		if ((int)c >= 48 && (int)c <= 57)
			return (1);
		return (0);
	}
	return (0);
}
